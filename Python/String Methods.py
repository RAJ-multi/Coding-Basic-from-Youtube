name = "Raj"
print(len(name))  # it will count the number of characters in the string
print(name.upper())
print(name.find("R")) # it will return the index of the character
print(name.find("r")) # it will return -1 if the character is not found
print(name.replace("R", "S")) # it will replace the character R with S
print('Raj' in name) # it will return True if the substring is found in the string else False
